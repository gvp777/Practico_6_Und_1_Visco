    private void jTextDniFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextDniFocusLost
        try{
        
           int valorJText = Integer.parseInt(jTextDni.getText());          //<---Si no Ingreso un numero que maneje el error . Hago qie el telefono se almacene en una variable int ya que si no es un numero lo que viene, que maneje ele error  (aunque el telefono lo almaceno como un string en el mapa)
        
        }catch(NumberFormatException e){
        
            if (!jTextDni.getText().equals("")){
                JOptionPane.showMessageDialog(this, "El campo 'D.N.I:' solo admite Numeros");
                jTextDni.requestFocus();
            }
        }  
        
    }//GEN-LAST:event_jTextDniFocusLost

