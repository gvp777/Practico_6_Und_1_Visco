    private void jBtGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtGuardarActionPerformed
       
        
        if (jTextDni.getText().equals("") ||                                      //<---Al intentar guardar, chequea si hay campos sin completar 
            jTextNombre.getText().equals("") ||
            jTextApellido.getText().equals("") ||
            jTextCiudad.getText().equals("") ||
            jTextDireccion.getText().equals("") ||   
            jTextTelefono.getText().equals("") ){
          
            JOptionPane.showMessageDialog(this, "No pueden quedar campos sin completar!");   
          
      }else{                                                                    //<---Sino, Guarde     
 
            long dni =  Long.parseLong(jTextDni.getText());                     //
            String nombre = jTextNombre.getText();                              //
            String apellido = jTextApellido.getText();                          // <---Recupero los valores de los jTextField   
            String ciudad = jTextCiudad.getText();                              //  
            String domicilio = jTextDireccion.getText();                        //
            String telefono = jTextTelefono.getText();                          //
            
                        
            Directorio directorio = Menu.getDirectorio();                       //<---Almaceno en un objeto direcroio el directorio que viene del metodo get del fromulario principal (sino la condicion del if se vuleve muy larga)

                
            if( directorio.agragarCliente (telefono, new Cliente(dni, nombre, apellido, domicilio, ciudad))){ //<--- Si el metrodo agregarCliente del directorio, me devuelve un true, entonces aviso con un JOptionPane. 

                JOptionPane.showMessageDialog(this, "Cliente Agregado Exitosamente!");
                limpairCamposDeTexto();
                jTextDni.requestFocus();
            }else{                                                              //<---Sino, tambien aviso     
                JOptionPane.showMessageDialog(this, "No se Agregro el Cliente" );
            }
            
        }
            
    }//GEN-LAST:event_jBtGuardarActionPerformed

